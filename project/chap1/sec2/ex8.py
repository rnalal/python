
# 사칙 연산 : +, -, *, /, //, **, %
a = 60
b = 14
c = "jeon"
d = "jaeyoung"
print("a+b=",a+b)
print("c+d=",c+d)
#print("a+c=",a+c)  #문자열과 숫자는 더하기 X
print("a-b=",a-b)
#print("c-d=",c-d)  #문자열은 뺄셈 불가
print("a*b=",a*b)
print("c*3=", c*3)      #문자열*숫자는 해당 숫자만큼 반복(repeat)
print("a/b=", a/b)      #나누기
print("a//b=", a//b)    #몫
print("a**b=", a**b)    #거듭제곱 = 60의 14승
print("a%b=", a%b)      #나머지



